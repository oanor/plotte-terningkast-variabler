controller.A.onEvent(ControllerButtonEvent.Pressed, function () {
    Ant_kast += 10
    for (let index = 0; index < 10; index++) {
        Kast = randint(1, 6)
        if (Kast == 1) {
            Enere += 1
        } else if (Kast == 2) {
            Toere += 1
        } else if (Kast == 3) {
            Treere += 1
        } else if (Kast == 4) {
            Firere += 1
        } else if (Kast == 5) {
            Femmere += 1
        } else if (Kast == 6) {
            Seksere += 1
        }
    }
    Terningposisjon()
})
